CREATE TABLE movies (
    shortname VARCHAR(50) PRIMARY KEY,
    title VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    rating VARCHAR(10) NOT NULL,
    language VARCHAR(50) NOT NULL,
    box_office_budget VARCHAR(10) NOT NULL,
    box_office_gross_us VARCHAR(10) NOT NULL,
    box_office_gross_worldwide VARCHAR(10) NOT NULL,
    box_office_opening_weekend_us VARCHAR(10) NOT NULL,
    box_office_opening_weekend_worldwide VARCHAR(10) NOT NULL,
    release_year INT NOT NULL,
    release_month INT NOT NULL,
    release_day INT NOT NULL,
    runtime_hours INT NOT NULL,
    runtime_minutes INT NOT NULL,
    runtime_seconds INT NOT NULL,
    director TEXT[] NOT NULL,
    producer TEXT[] NOT NULL,
    writer TEXT[] NOT NULL,
    "cast" TEXT[] NOT NULL,
    genre TEXT[] NOT NULL,
    distributor TEXT[] NOT NULL,
    country_of_origin TEXT[] NOT NULL
);